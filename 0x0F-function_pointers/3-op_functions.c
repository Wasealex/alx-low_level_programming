#include "calc.h"
/**
 *op_add - add 2 number
 *@a: first number
 *@b: second number
 *Return: result
 */
int op_add(int a, int b)
{
	return (a + b);
}
/**
 *op_sub - sub 2 number
 *@a: first number
 *@b: second number
 *Return: result
 */
int op_sub(int a, int b)
{
	return (a - b);
}
/**
 *op_mul - mul 2 number
 *@a: first number
 *@b: second number
 *Return: result
 */
int op_mul(int a, int b)
{
	return (a * b);
}
/**
 *op_div - divide 2 number
 *@a: first number
 *@b: second number
 *Return: result
 */
int op_div(int a, int b)
{
	return (a / b);
}
/**
 *op_mod - modulo 2 number
 *@a: first number
 *@b: second number
 *Return: result
 */
int op_mod(int a, int b)
{
	return (a % b);
}
